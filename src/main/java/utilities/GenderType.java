package utilities;

/**
 * Representerar möjliga könstillstånd för en person.
 *
 * Denna enum används för att definiera och begränsa de tillåtna könsalternativen
 * i systemet till Male, Female och Unknown.
 */
public enum GenderType {
    /** Representerar en manlig person. */
    MALE,

    /** Representerar en kvinnlig person. */
    FEMALE,

    /** Representerar ett okänt eller ospecificerat kön. */
    UNKNOWN;
}
